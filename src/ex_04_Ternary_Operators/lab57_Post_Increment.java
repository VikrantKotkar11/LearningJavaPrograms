package ex_04_Ternary_Operators;

public class lab57_Post_Increment {
    public static void main(String[] args) {

        //      Post Increment = a++
        // value will store first and then Increment
        int a=10;
        int b=a++;
        System.out.println(a);
        System.out.println(b);

        //line no | a | b
        //      8 | 10| NA
        //      9 | 10| 10
        //      10| 11| 10

    }
}

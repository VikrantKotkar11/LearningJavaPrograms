package ex_03_Operators;

public class lab43_ex03_IMP {
    public static void main(String[] args) {
        char a1='A';    // ASCII = 65
        char a2='B';    // ASCII = 66
        System.out.println(a1+a2);// concat happends only in case of String...


        System.out.println('A'==65);    //'A' = char, ASCII=65;


        short s=10;
        char c='A';
        System.out.println(c);
        System.out.println(c+s);// it's act as ASCII value when you concat..



    }
}

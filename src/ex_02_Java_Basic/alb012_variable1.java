package ex_02_Java_Basic;

public class alb012_variable1 {
    public static void main(String[] args) {
        //the variable name can be below
        int a=10;
        int $=11;
        int _abc=12;
        int this_is_a_very_very_long_name_in_the_class_$_abc=13;
        System.out.println(a);
        System.out.println($);
        System.out.println(_abc);
        System.out.println(this_is_a_very_very_long_name_in_the_class_$_abc);
    }
}

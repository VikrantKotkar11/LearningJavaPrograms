package ex_02_Java_Basic;

public class lab028_Literals_NonPrimitive {
    public static void main(String[] args) {
        //Primitive
        byte b=10;//.....size=8
        int a=65;//.....size=32

        //Non-Primitive
        String name= "Vikrant";//Default_value =null;   //.....size=64
        System.out.println(name);
    }
}

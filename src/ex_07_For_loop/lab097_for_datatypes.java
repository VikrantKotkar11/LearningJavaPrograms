package ex_07_For_loop;

public class lab097_for_datatypes {
    public static void main(String[] args) {
        // with float datatype
        for (float a = 0.0f; a < 5.2f; a++) {
            System.out.println("Hello float");
        }

        //  with long datatype
        for (long i=1; i<5;i++)
        {
            System.out.println("Hii");
        }
    }
}